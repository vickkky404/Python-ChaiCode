# Program 4: If-Else Conditional
print("Grade Calculator")
marks = float(input("Enter marks: "))
if marks >= 90:
    print("Grade: A+")
elif marks >= 80:
    print("Grade: A")
elif marks >= 70:
    print("Grade: B")
elif marks >= 60:
    print("Grade: C")
else:
    print("Grade: F")
if marks >= 40:
    print("Status: PASS")
else:
    print("Status: FAIL")

# Program 12: Math Operations
import math

print("Basic Arithmetic:")
print(f"10 + 5 = {10 + 5}")
print(f"10 - 5 = {10 - 5}")
print(f"10 * 5 = {10 * 5}")
print(f"10 / 5 = {10 / 5}")
print(f"10 // 3 = {10 // 3}")
print(f"10 % 3 = {10 % 3}")
print(f"2 ** 3 = {2 ** 3}")

print("\nMath Module:")
print(f"Square root of 16: {math.sqrt(16)}")
print(f"Absolute value of -10: {abs(-10)}")
print(f"Maximum of 5, 10, 3: {max(5, 10, 3)}")
print(f"Minimum of 5, 10, 3: {min(5, 10, 3)}")
print(f"Pi value: {math.pi}")
print(f"Sin(30 degrees): {math.sin(math.radians(30))}")

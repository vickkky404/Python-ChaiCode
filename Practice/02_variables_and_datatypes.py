# Program 2: Variables and Data Types
# Demonstrates how to declare and use variables

# Integer
age = 25
student_id = 12345

# Float
height = 5.9
temperature = 36.5

# String
name = "Vikram"
city = "Odisha"

# Boolean
is_student = True
is_working = False

# Printing variables
print(f"Name: {name}")
print(f"Age: {age}")
print(f"Height: {height} feet")
print(f"Is Student: {is_student}")

# Type checking
print(f"\nData Types:")
print(f"Type of age: {type(age)}")
print(f"Type of height: {type(height)}")
print(f"Type of name: {type(name)}")
print(f"Type of is_student: {type(is_student)}")
